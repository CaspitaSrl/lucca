<template>
  <div>
    <p>{{ msg }}</p>
  </div>
</template>

<script>
import axios from 'axios';

export default {
  name: 'Ping',
  data() {
    return {
      msg: '',
    };
  },
  methods: {
    getMessage: () => {
      console.log(this.$api_gcloud);
      const url = this.$api_gcloud + '/ping';
      axios.get(url)
        .then((res) => {
          this.msg = res.data;
        })
        .catch((error) => {
          // eslint-disable-next-line
          console.error(error);
        });
    },
  },
  created() {
    console.log(this.msg);
    this.getMessage();
  },
};
</script>
